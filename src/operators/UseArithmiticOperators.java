package operators;

public class UseArithmiticOperators {
	
	// + Additive operator
	// - Subtraction
	// * Multiplication
	// / Division
	// % Reminder
	
	public static void main(String[] args) {
		int number1=50;
		int number2=60;
		
		int summation=number1+number2;
		System.out.println("Summation value is " +summation);
		
		
		int substruction=number2+number1;
		System.out.println("Substruction value is " +substruction);
		
		
		int multiply=number1*number2;
		System.out.println("Multiplication value is " +multiply);
		
		
		double division=number1/number2;
		System.out.println("Division value is " +division);
		
		double reminder=number1%number2;
		System.out.println("Reminder value is " +reminder);
	}
	
	
	

}
